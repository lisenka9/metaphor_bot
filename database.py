                can_take = days_since_last >= 7
                days_until_next = max(0, 7 - days_since_last) if not can_take else 0
                
                return {
                    'has_subscription': False,
                    'last_message_date': last_date,
                    'can_take': can_take,
                    'days_until_next': days_until_next
                }
                
