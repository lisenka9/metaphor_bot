        if stats:
            if stats['has_subscription']:
                limit_text = f"‚ùå {reason}\n\nüìä –°–µ–≥–æ–¥–Ω—è: {stats['today_count']}/5 –ø–æ—Å–ª–∞–Ω–∏–π"
                reply_markup = keyboard.get_main_menu_keyboard()
            else:
                if stats['can_take']:
                    limit_text = "‚úÖ –ú–æ–∂–Ω–æ –≤–∑—è—Ç—å –ø–æ—Å–ª–∞–Ω–∏–µ (1 —Ä–∞–∑ –≤ –Ω–µ–¥–µ–ª—é)"
